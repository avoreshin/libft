#include "libft.h"

/*
** Функция isalnum() проверяет любой символ, для которого isalpha(3)
** или isdigit(3) истинны.
**
** RETURN
**
** Функция isalnum() возвращает ноль, если символ тестирует false,
** и возвращает ненулевое значение, если символ тестирует true.
*/

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
		return (1);
	return (0);
}
